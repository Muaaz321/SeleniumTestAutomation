package Core;

public class A {

	public A () {
		System.out.println("This is A default constructor");
	}
	
	
	public A (int x) {
		System.out.println("This is A constructor with : "+x+" :  parameter");
	}
	
	public A (String c) {
		System.out.println("This is A constructor with : "+c+" :  parameter");
	}
	
	public A (int z,String a) {
		System.out.println("This is A constructor with : "+z+" : "+a+":  parameter");
	}
	
}
