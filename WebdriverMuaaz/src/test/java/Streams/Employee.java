package Streams;

public class Employee {
	
	private String name;
	private Double Salary;
	
	public Employee(String name,Double salary) {
		
		this.Salary = salary;
		this.name=name;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Double getSalary() {
		return Salary;
	}

	public void setSalary(Double salary) {
		this.Salary = salary;
	}

	
	
	
	
	
	
	
	
}
